#include "../inc/cub3d.h"

long	ft_timestamp(void)
{
	struct timeval	tm;

	if (gettimeofday(&tm, NULL))
		return (-1);
	return ((tm.tv_sec * 1000) + (tm.tv_usec * 0.001));
}
